    Vector<Patient> patients=new Vector();
    DefaultTableModel tm=new DefaultTableModel();
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Patient p=new Patient(Integer.parseInt(txtId.getText()), 
                              txtFirstName.getText(), 
                              txtLastName.getText()  , 
                              cbDoctor.getSelectedItem().toString());
        patients.add(p);
        
        tm.addRow(new String[]{txtId.getText(), 
                              txtFirstName.getText(), 
                              txtLastName.getText()  , 
                              cbDoctor.getSelectedItem().toString()});
    }//GEN-LAST:event_jButton1ActionPerformed
    void showPatients(){
    
      tm.setRowCount(0);
        for (Patient patient : patients) {
            if (patient.doctorName.equals(cbDoctor.getSelectedItem().toString())){
                tm.addRow(new String[]{patient.id+"", 
                                patient.firstName, 
                                patient.lastName, 
                               patient.doctorName});
            }
        }
    }
